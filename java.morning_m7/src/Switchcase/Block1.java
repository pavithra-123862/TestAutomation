package Switchcase;

public class Block1 {
	public static void main(String[]  args)
	 {
	  char a='A';
	  switch(a)
	  {
	   case 'A':System.out.println('A');
	            break;
	   case 'B':System.out.println('B');	    
	            break;
	   case 'C':System.out.println('C');
	            break;
	   case 'D':System.out.println('D');
	            break;
	   default :System.out.println("invalid input");
	 }
	 }

}
